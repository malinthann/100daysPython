# Comparison Operators
# > Greater than (10 > 20 is False)
# < Less than (10 < 20 is True)
# >= Greater than or equal to (10 >= 20 is False)
# <= Less than or equal to (10 <= 20 is True)
# == Equal to (10 == 20 is False)
# != Not equal to (10 != 20 is True)
# % is the modulus operator. It calculates the remainder of the division of one number by another.

# Example if/else statement
water_level = 50
if water_level > 80:
    print("Drain water")
else:
    print("Continue")


# Example if/else statement
print("Welcome to the rollercoaster!")
height = float(input("Enter your height in cm: "))

if height >= 120:
    print("You can ride the rollercoaster!")  # True
else:
    print("Sorry, you have to grow taller before you can ride.")  # False
